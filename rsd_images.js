function _normalize(s) {
  if (!s || typeof s !== 'string') return '';
  return s.toLowerCase()
    .replace(/^the\s+/i, '')
    .replace(/\s+feat\.?\s+/g, ' & ')
    .replace(/\s+featuring\s+/g, ' & ')
    .replace(/\s+and\s+/g, ' & ')
    .replace(/[\'\u2019]/g, '')
    .replace(/[-\u2013\u2014]/g, ' ')
    .replace(/\s+/g, ' ')
    .replace(/\(deluxe[^)]*\)|\(expanded[^)]*\)|\(remastered[^)]*\)|\(rsd[^)]*\)| ep\b| \d+th anniversary[^)]*\)?/gi, '')
    .replace(/[^\w\s&-]/g, ' ')
    .replace(/\s+/g, ' ')
    .trim();
}

function _coreTitle(t) {
  const n = _normalize(t);
  const dash = n.indexOf(' - ');
  const paren = n.indexOf(' (');
  const cut = Math.min(dash >= 0 ? dash : 999, paren >= 0 ? paren : 999);
  return (cut < 999 ? n.slice(0, cut) : n).slice(0, 50);
}

function _titleOverlap(a, b) {
  if (!a || !b) return false;
  if (a === b || a.indexOf(b) >= 0 || b.indexOf(a) >= 0) return true;
  const aWords = a.split(/\s+/).filter(w => w.length > 2);
  const bWords = b.split(/\s+/).filter(w => w.length > 2);
  for (let i = 0; i < aWords.length; i++) {
    for (let j = 0; j < bWords.length; j++) {
      if (aWords[i].length >= 4 && aWords[i] === bWords[j]) return true;
    }
  }
  return a.length >= 10 && b.length >= 10 && (a.slice(0, 12) === b.slice(0, 12) || a.slice(0, 15).indexOf(b.slice(0, 15)) >= 0 || b.slice(0, 15).indexOf(a.slice(0, 15)) >= 0);
}

function getRSDImageUrl(artist, title) {
  const k = (artist || '').trim() + '|' + (title || '').trim();
  let url = RSD_IMAGE_MAP[k] || RSD_IMAGE_MAP[k.toLowerCase()] || null;
  if (url) return url;
  const nArtist = _normalize(artist);
  const nTitle = _normalize(title);
  const coreT = _coreTitle(title);
  if (!nArtist || coreT.length < 3) return null;
  const keys = Object.keys(RSD_IMAGE_MAP);
  for (let i = 0; i < keys.length; i++) {
    const key = keys[i];
    if (key === key.toLowerCase()) continue;
    const pipe = key.indexOf('|');
    if (pipe < 0) continue;
    const mArtist = key.slice(0, pipe);
    const mTitle = key.slice(pipe + 1);
    if (!mArtist || !mTitle) continue;
    const nMArtist = _normalize(mArtist);
    const nmTitle = _normalize(mTitle);
    const mCore = _coreTitle(mTitle);
    if (nMArtist !== nArtist) continue;
    if (nTitle === nmTitle || coreT === mCore) return RSD_IMAGE_MAP[key];
    if (_titleOverlap(coreT, mCore)) return RSD_IMAGE_MAP[key];
    if (coreT.length >= 8 && nmTitle.indexOf(coreT.slice(0, 18)) >= 0) return RSD_IMAGE_MAP[key];
    if (mCore.length >= 8 && nTitle.indexOf(mCore.slice(0, 18)) >= 0) return RSD_IMAGE_MAP[key];
  }
  return null;
}

